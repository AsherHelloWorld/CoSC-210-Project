public class Task{
    String name;
    int date;
    int time;

    public Task(String name, int date, int time){
        name = name;
        date = date;
        time = time;
    }

    private getName(){
        return this.name;
    }
    private void setName(String name){
        this.name = name;
    }
    private getDate(){
        return this.date;
    }
    private void setDate(int date){
        this.date = date;
    }
    private getTime(){
        return this.time;
    }
    private void setTime(int time){
        this.time = time;
    }






}